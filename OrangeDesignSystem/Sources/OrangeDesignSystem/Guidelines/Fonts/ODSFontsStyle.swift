import SwiftUI

///
/// Defines all styles supported for Text elements.
/// The right font associated to the style is provider by
/// the `ODSTheme`.
///
public enum ODSFontStyle: String, CaseIterable {
    case headlineL
    case headlineS
    case titleL
    case titleM
    case titleS
    case bodyLBold
    case bodyLRegular
    case bodyM
    case bodyS
    case labelL
    case labelMBold
    case labelMRegular
    case labelS
}
